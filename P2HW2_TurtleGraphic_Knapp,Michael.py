#Start Turtle
import turtle
turtle.showturtle()
#Begin Drawing directions
turtle.forward(100)
turtle.forward(100)
turtle.write('East')
turtle.left(180)
turtle.forward(400)
turtle.write('West')
turtle.left(180)
turtle.forward(200)
turtle.left(90)
turtle.forward(200)
turtle.write('North')
turtle.left(180)
turtle.forward(400)
turtle.write('South')
#Set turtle back to center
turtle.left(180)
turtle.forward(200)
#Draw Circle
turtle.right(90)
turtle.forward(50)
turtle.left(90)
turtle.circle(50)
